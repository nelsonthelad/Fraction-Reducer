    public static int greatestCommonDivisor(int num, int dem) {
        if (dem == 0) {  // If one number is zero, the other number is the greatest commom divisor
            return num;
        }
        
        return greatestCommonDivisor(dem, num % dem);  // calls the function with swapped arguments 
    }
    
    
