    private void calcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcActionPerformed
        try {
            int num = Integer.valueOf(numInput.getText()); // creates the num variable using the text from numInput
            int dem = Integer.valueOf(demInput.getText()); // creates the dem variable using the text from demInput
            
        
            int gcd = greatestCommonDivisor(num, dem); // uses the greatestCommonDivisor method to find the gcd and stores it as the variable gcd
        
            int finalNum = num/gcd; // creates variables finalNum and finalDem by dividing the num and dem by the gcd
            int finalDem = dem/gcd;
        
            
            if (finalDem == 1) { // if finalDem = 1 then this means we can write the fraction as a whole number. This is shown in the ouput box
                output.setText("The Reduced Fraction to Lowest Terms is " + finalNum + "/" + finalDem + " or just " + finalNum);
            }
            else { // if the fraction cannot be converted to a whole number
                output.setText("The Reduced Fraction to Lowest Terms is " + finalNum + "/" + finalDem);
            }
        
            System.out.println(finalNum + "/" + finalDem); // prints info to console
            System.out.println(gcd);
        }
        catch (Exception e) {
            output.setText("Please Enter Integers Only!"); // if an error is caught prints message to output box
        }
    }//GEN-LAST:event_calcActionPerformed

